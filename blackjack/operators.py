# Add
def add(a, b):
    """Adds two numbers"""
    return a + b

# Subtract
def subtract(a, b):
    """Subtracts two numbers"""
    return a - b

# Multiply
def multiply(a, b):
    """Multiplies two numbers"""
    return a * b

# Divide
def divide(a, b):
    """Divides two numbers"""
    return a / b